<h1>Data Deletion Instructions</h1>

<p><strong>Effective Date:</strong> April 2026</p>

<h2>How to Delete Your Data</h2>

<p>If you want to delete your account and personal data from PDMS, follow one of the methods below:</p>

<h3>Method 1: Email Request</h3>
<p>Send a request to:</p>
<p><strong>support@pdms.developers.ink</strong></p>
<p>Include your registered email address. We will delete your data within 7 working days.</p>

<h3>Method 2: In-App Request (if available)</h3>
<p>Go to your profile → Settings → Delete Account</p>

<h2>What Data Will Be Deleted</h2>
<ul>
    <li>Profile information</li>
    <li>Login account (Google/Facebook linked data)</li>
    <li>Appointments and related records</li>
</ul>

<h2>Retention Policy</h2>
<p>Some records may be retained for legal or security purposes, but personal identifiers will be removed.</p>

